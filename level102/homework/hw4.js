//შეკრიბეთ ეს ქულები.

//იპოვოს საშუალო და შეინახხეთ ცვლადში

//შემდეგ --> if else ით შეამოწმეთ

//თუ  საშუალო ≥ 51 → "დადებითი შედეგი".

//სხვა შემთხვევაში → "უარყოფითი შედეგი".


function result(p1, p2, p3) {
  let sum = p1 + p2 + p3;
  let average = sum / 3;

  if (average >= 51) {
    console.log("დადებითი შედეგი");
  } else {
    console.log("უარყოფითი შედეგი");
  }
}

result(60, 45, 55); 
