let num = prompt(" გთხოვთ შეიყვანეთ რიცხვი:")
num = parseInt(num) 

let i = 1
while (i <= num) {
    if (i % 2 === 0) {
        console.log(i + " - ლუწია")
    } else {
        console.log(i + " - კენტია")
    }
    

    i = i + 1
}
